def sum_function(number_1: float, number_2: float) -> float:
    """summing two numbers

    Args:
        number_1 (float): any number in the reals
        number_2 (float): any number in the reals

    Returns:
        float: a number that belongs to the real
    """
    sum_two_numbers = number_1 + number_2
    return sum_two_numbers
